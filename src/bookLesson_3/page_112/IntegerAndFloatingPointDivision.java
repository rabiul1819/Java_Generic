package bookLesson_3.page_112;

public class IntegerAndFloatingPointDivision {
    public static void main(String[] args) {

        System.out.println(4 + 5);
        System.out.println(4 - 5);
        System.out.println(4 - 5 * 9);
        System.out.println(3 / 7);
        System.out.println(3 / 7.0);
        System.out.println(59 * 100 / 60.0);

        double y = 1 / 3;
        double x = 1.0 / 3;
        System.out.println(y);
        System.out.println(x);
    }
}
